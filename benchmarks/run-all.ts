/**
 * Benchmark Runner — Executes all benchmark suites and collects results.
 *
 * Usage:
 *   npx tsx run-all.ts              # Run all and print to stdout
 *   npx tsx run-all.ts --save       # Also write results to RESULTS.md
 */

import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const saveResults = process.argv.includes('--save');

const suites = [
  { name: 'Error Matching', file: 'error-matching.bench.ts' },
  { name: 'Learning Cycle', file: 'learning-cycle.bench.ts' },
  { name: 'IPC Round-Trip', file: 'ipc-roundtrip.bench.ts' },
  { name: 'Embedding Engine', file: 'embedding-coldstart.bench.ts' },
];

const banner = `
╔═══════════════════════════════════════════════════════════════════╗
║               Brain Ecosystem — Performance Benchmarks           ║
╠═══════════════════════════════════════════════════════════════════╣
║  Platform:  ${process.platform} ${process.arch}${' '.repeat(Math.max(0, 40 - process.platform.length - process.arch.length))}║
║  Node.js:   ${process.version}${' '.repeat(Math.max(0, 40 - process.version.length))}║
║  Date:      ${new Date().toISOString().slice(0, 19)}${' '.repeat(21)}║
║  CPUs:      ${(await import('node:os')).cpus().length} cores${' '.repeat(34)}║
╚═══════════════════════════════════════════════════════════════════╝
`;

console.log(banner);

const allOutput: string[] = [];
const startTime = performance.now();

for (const suite of suites) {
  const filePath = path.join(__dirname, suite.file);
  console.log(`\n  Running: ${suite.name} (${suite.file})`);
  console.log('  ' + '-'.repeat(60));

  try {
    const output = execSync(
      `node --import tsx "${filePath}"`,
      {
        cwd: __dirname,
        encoding: 'utf8',
        stdio: ['pipe', 'pipe', 'pipe'],
        timeout: 300_000, // 5 minute timeout per suite
        env: {
          ...process.env,
          NODE_NO_WARNINGS: '1',
        },
      },
    );
    console.log(output);
    allOutput.push(output);
  } catch (err) {
    const error = err as { stdout?: string; stderr?: string; message?: string };
    console.error(`  FAILED: ${suite.name}`);
    if (error.stdout) console.log(error.stdout);
    if (error.stderr) console.error(error.stderr);
    allOutput.push(`\n## ${suite.name}\n\nFailed: ${error.message ?? 'Unknown error'}\n`);
  }
}

const totalMs = performance.now() - startTime;

console.log('\n' + '='.repeat(70));
console.log(`  All benchmarks complete in ${(totalMs / 1000).toFixed(1)}s`);
console.log('='.repeat(70));

// --- Optionally save to RESULTS.md ---

if (saveResults) {
  const os = await import('node:os');
  const cpus = os.cpus();
  const cpuModel = cpus[0]?.model ?? 'Unknown';
  const totalMem = (os.totalmem() / (1024 * 1024 * 1024)).toFixed(1);

  const markdown = `# Brain Ecosystem — Performance Benchmark Results

> Auto-generated by \`benchmarks/run-all.ts\` on ${new Date().toISOString().slice(0, 19)}

## Environment

| Property | Value |
|----------|-------|
| Platform | ${process.platform} ${process.arch} |
| Node.js | ${process.version} |
| CPU | ${cpuModel} |
| CPU Cores | ${cpus.length} |
| Total RAM | ${totalMem} GB |
| Date | ${new Date().toISOString().slice(0, 19)} |
| Total Duration | ${(totalMs / 1000).toFixed(1)}s |

---

${allOutput.join('\n')}

---

## Notes

- All timings use \`performance.now()\` for sub-millisecond precision
- SQLite benchmarks use in-memory databases (no disk I/O)
- Embedding model benchmarks require \`@huggingface/transformers\` to be installed
- Results vary by machine; these numbers are for relative comparison only
- Each benchmark includes a warmup phase that is excluded from measurements
`;

  const resultsPath = path.join(__dirname, 'RESULTS.md');
  writeFileSync(resultsPath, markdown, 'utf8');
  console.log(`\n  Results saved to: ${resultsPath}`);
}
